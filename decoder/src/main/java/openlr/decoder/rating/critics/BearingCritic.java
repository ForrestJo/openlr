package openlr.decoder.rating.critics;

import openlr.decoder.properties.OpenLRDecoderProperties;
import openlr.map.Line;
import openlr.map.utils.GeometryUtils;
import openlr.map.utils.GeometryUtils.BearingDirection;

public class BearingCritic {
    private final RatingFunction ratingFunction;
    private final int bearingDistance;

    private BearingCritic(final RatingFunction ratingFunction, final int bearingDistance) {
        this.ratingFunction = ratingFunction;
        this.bearingDistance = bearingDistance;
    }

    public static BearingCritic with(final OpenLRDecoderProperties openLRDecoderProperties) {
        RatingFunction linearRating = new LinearRating(openLRDecoderProperties.getMaxBearingDiff(), openLRDecoderProperties.getMaxBearingScore());
        return new BearingCritic(linearRating, openLRDecoderProperties.getBearingDistance());
    }

    public int rate(final Line line, final boolean isLastLrp, final double lrpBearing, final int projectionAlongLine) {
        BearingDirection direction = BearingDirection.IN_DIRECTION;
        if (isLastLrp) {
            direction = BearingDirection.AGAINST_DIRECTION;
        }
        double lineBearing = GeometryUtils.calculateLineBearing(line, direction,
                bearingDistance, projectionAlongLine);
        double difference = GeometryUtils.bearingDifference(lineBearing, lrpBearing);
        return ratingFunction.rate(difference);
    }
}
